    private void populateCourseTable() {
        Object[] rowInfo= new Object[2];
        ArrayList<Course> courseList=courseList=Course.getAllCourses();
        
        //adding all the course of the current user tothe table
        for(Course thisCourse: courseList){
            DefaultTableModel modelofThistable=(DefaultTableModel) tblCourses.getModel();
            rowInfo[0]=thisCourse.getID();
            rowInfo[1]=thisCourse.getTitle();
            modelofThistable.addRow(rowInfo);
            
        }
        
        tblCourses.setFillsViewportHeight(true);
    }

    private void populateStudentTable(Course course) {
        DefaultTableModel modelofThistable=(DefaultTableModel) tblParticipants.getModel();
        modelofThistable.setRowCount(0);//clearing previous data
        Object[] rowInfo= new Object[4];
        ArrayList<Student> studentsOfCourse= course.getStudentsOfCourse();
        lblParticipants.setText("Course Participants: "+ studentsOfCourse.size()+" Student(s)");
        //adding all the course of the current user tothe table
        for(Student thisStudent: studentsOfCourse){
            
            rowInfo[0]=thisStudent.getName();
            rowInfo[1]=thisStudent.getSurname();
            rowInfo[2]=thisStudent.getMajor();
            rowInfo[3]=thisStudent.getMinor();
            modelofThistable.addRow(rowInfo);
        
        }   
        
        tblParticipants.setFillsViewportHeight(true);
        
    }

