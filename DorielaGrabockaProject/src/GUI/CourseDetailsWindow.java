    private void btnDisplayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDisplayActionPerformed
        // TODO add your handling code here:
        lblError.setVisible(false);
        try{
            String s=txtCourseID.getText();//user input in the textfield
            int courseid=Integer.parseInt(s);//user input converted to integer
            //readData(courseid);//reads data from the DataReader and places them on the fields
            Course course=Course.getCourse(courseid);
            if(course!=null){
                fillLabels(course);
                populateStudentTable(course);
            }
            else{
                lblError.setText("This course does not exist! Enter again!");
                lblError.setForeground(Color.WHITE);
                lblError.setVisible(true);
            }
        }
        catch(NumberFormatException e){
            lblError.setForeground(Color.white);
            lblError.setVisible(true);//input not numeric
        }
    }//GEN-LAST:event_btnDisplayActionPerformed

    private void tblCoursesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCoursesMouseClicked
        // TODO add your handling code here:
        int index=tblCourses.getSelectedRow();
        TableModel model=tblCourses.getModel();
        int courseID=Integer.parseInt(model.getValueAt(index, 0).toString());//get the ID of the selected row
        Course course= Course.getCourse(courseID);
        fillLabels(course);
        populateStudentTable(course);
        txtCourseID.setText(new Integer (courseID).toString());
    }//GEN-LAST:event_tblCoursesMouseClicked

